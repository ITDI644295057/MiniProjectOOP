    void loadCustomerData() {
        try {
            String sql = "SELECT*FROM customer";
            ArrayList<Customer> list = myDB.selectQuery(sql);
            for (Customer customer : list) {
                String[] rowData = {customer.getId(), customer.getName(), customer.getSurname(), customer.getRoomType(),
                    customer.getPeriod(), customer.getDays(), customer.getTotalPrice()};
                model.addRow(rowData);
                tblData.setModel(model);
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }

