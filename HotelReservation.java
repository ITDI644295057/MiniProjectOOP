    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String roomType = "";
        int price = 0;
        String id = txtId.getText();
        String name = txtName.getText();
        String surname = txtSurname.getText();
        String period = cbxWeek.getSelectedItem().toString();
        int days = (int) spnDays.getValue();
        if (id.length() > 0 && name.length() > 0 && surname.length() > 0 && days != 0
                && cbxWeek.getSelectedIndex() != 0 && btnGroup.getSelection() != null) {
            if (rdoSuperior.isSelected()) {
                roomType = "Superior";
                if (cbxWeek.getSelectedIndex() == 1) {
                    price = 2090;
                } else if (cbxWeek.getSelectedIndex() == 2) {
                    price = 2490;
                } else {//LongWeekend
                    price = 3390;
                }
            } else if (rdoDeluxe.isSelected()) {
                roomType = "Deluxe";
                if (cbxWeek.getSelectedIndex() == 1) {
                    price = 2290;
                } else if (cbxWeek.getSelectedIndex() == 2) {
                    price = 2790;
                } else {//LongWeekend
                    price = 3690;
                }
            } else if (rdoGrandDeluxe.isSelected()) {
                roomType = "Grand Deluxe";
                if (cbxWeek.getSelectedIndex() == 1) {
                    price = 3890;
                } else if (cbxWeek.getSelectedIndex() == 2) {
                    price = 4490;
                } else {//LongWeekend
                    price = 5390;
                }
            } else if (rdoJuniorSuit.isSelected()) {
                roomType = "Junior Suit";
                if (cbxWeek.getSelectedIndex() == 1) {
                    price = 3190;
                } else if (cbxWeek.getSelectedIndex() == 2) {
                    price = 3890;
                } else {//LongWeekend
                    price = 4790;
                }
            } else if (rdoFamilyJuniorSuit.isSelected()) {
                roomType = "Family Junior Suit";
                if (cbxWeek.getSelectedIndex() == 1) {
                    price = 3990;
                } else if (cbxWeek.getSelectedIndex() == 1) {
                    price = 4690;
                } else {//LongWeekend
                    price = 5690;
                }
            } else if (rdoGrandFamilySuite.isSelected()) {
                roomType = "Grand Family Suite";
                if (cbxWeek.getSelectedIndex() == 1) {
                    price = 4690;
                } else if (cbxWeek.getSelectedIndex() == 2) {
                    price = 5590;
                } else {//LongWeekend
                    price = 6490;
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "กรุณาเลือกประเภทห้องพักครับ");
            }
            int total = price * days;
            String rowData[] = {id, name, surname, roomType, period, String.valueOf(days), String.valueOf(total)};
            String sql = "INSERT INTO customer VALUES ('" + id + "','" + name + "','" + surname + "',"
                    + "'" + roomType + "','" + period + "','" + days + "','" + total + "')";
            int row = myDB.CUD(sql);
            if (row > 0) {
                model.addRow(rowData);
                tarResult.setText("ประเภทห้องพัก : " + roomType + "\n" + "ช่วงที่เข้าพัก : " + period + "\n"
                        + "จำนวนวันเข้าพัก : " + days + " วัน" + "\n" + "รวมราคา : " + total + " บาท");
                btnAdd.setEnabled(false);
            } else {
                JOptionPane.showMessageDialog(rootPane, "ไม่สามารถเพิ่มข้อมูลได้");
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "กรุณากรอกข้อมูลให้ครบก่อนครับ");
        }
    }//GEN-LAST:event_btnAddActionPerformed

