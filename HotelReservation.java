    private void tblDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDataMouseClicked
        btnAdd.setEnabled(false);
        int row = tblData.getSelectedRow();
        if (row >= 0) {
            String id = tblData.getValueAt(row, 0).toString();
            String name = tblData.getValueAt(row, 1).toString();
            String surname = tblData.getValueAt(row, 2).toString();
            String roomType = (String) tblData.getValueAt(row, 3).toString();
            String period = tblData.getValueAt(row, 4).toString();
            String days = tblData.getValueAt(row, 5).toString();
            String totalPrice = tblData.getValueAt(row, 6).toString();
            txtId.setText(id);
            txtName.setText(name);
            txtSurname.setText(surname);
            switch (roomType) {
                case "Superior":
                    rdoSuperior.setSelected(true);
                    break;
                case "Deluxe":
                    rdoDeluxe.setSelected(true);
                    break;
                case "Grand Deluxe":
                    rdoGrandDeluxe.setSelected(true);
                    break;
                case "Junior Suit":
                    rdoJuniorSuit.setSelected(true);
                    break;
                case "Family Junior Suit":
                    rdoFamilyJuniorSuit.setSelected(true);
                    break;
                case "Grand Family Suite":
                    rdoGrandFamilySuite.setSelected(true);
                    break;
            }
            switch (period) {
                case "Weekday (Sun - Thu)":
                    cbxWeek.setSelectedIndex(1);
                    break;
                case "Weekend (Fri - Sat)":
                    cbxWeek.setSelectedIndex(2);
                    break;
                default:
                    cbxWeek.setSelectedIndex(3);
                    break;
            }
            int day = Integer.valueOf(days);
            spnDays.setValue(day);
            tarResult.setText("ประเภทห้องพัก : " + roomType + "\n" + "ช่วงที่เข้าพัก : " + period + "\n"
                        + "จำนวนวันเข้าพัก : " + days + " วัน" + "\n" + "รวมราคา : " + totalPrice + " บาท");
        }
    }//GEN-LAST:event_tblDataMouseClicked

